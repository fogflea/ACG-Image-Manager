class I18N:
    def __init__(self):
        self._lang = "en"

    def set_language(self, lang: str) -> None:
        self._lang = lang if lang in STRINGS else "en"

    def get_language(self) -> str:
        return self._lang

    def tr(self, key: str, **kwargs) -> str:
        base = STRINGS.get(self._lang, STRINGS["en"])
        text = base.get(key, STRINGS["en"].get(key, key))
        return text.format(**kwargs) if kwargs else text

    def load_from_settings(self, settings: QSettings, key: str, default: str = "en") -> None:
        self.set_language(str(settings.value(key, default)))


i18n = I18N()
