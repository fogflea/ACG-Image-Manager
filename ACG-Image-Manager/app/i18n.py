    def set_language(self, language: str) -> None:
        normalized = "zh_CN" if language.lower().startswith("zh") else "en"
        if normalized != self._language:
            self._language = normalized
            self.language_changed.emit(self._language)

    def language(self) -> str:
        return self._language

    def tr(self, key: str, **kwargs) -> str:
        table = self._translations.get(self._language, self._translations["en"])
        template = table.get(key, self._translations["en"].get(key, key))
        return template.format(**kwargs)


